animal = " chanCHito feliz "

print(animal.upper()) # Uppercase
print(animal.lower()) # Lowercase
print(animal.capitalize()) # Capitalize
print(animal.title()) # Pascal case
print(animal.strip()) # Similar to trim
print(animal.strip().capitalize()) 
print(animal.find("CH"))
print(animal.find("cH"))
print(animal.replace("CH", "j"))
print("nCH" in animal)
print("nCH" not in animal)
